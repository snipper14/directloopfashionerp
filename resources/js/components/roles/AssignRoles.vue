        async assignRoles() {
            let data = JSON.stringify(this.resources);
            const res = await this.callApi("post", "data/admin/assign_roles", {
                permission: data,
                id: this.form_data.id,
            });
            if (res.status == 200) {
                this.s("Role has been assigned successfully!");
                let index = this.role_data.findIndex(
                    (role) => role.id == this.form_data.id
                );
                this.role_data[index].permission = data;
            } else {
                this.swr();
            }
        },
        changeAdmin() {
            let index = this.role_data.findIndex(
                (role) => role.id == this.form_data.id
            );
            let permission = this.role_data[index].permission;
            if (!permission) {
                this.resources = this.defaultResourcesPermission;
            } else {
                this.resources = JSON.parse(permission);
            }
        },
